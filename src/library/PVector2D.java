package library;

public class PVector2D {
	private double x;
	private double y;
	
	
	public PVector2D(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
	
	
}
